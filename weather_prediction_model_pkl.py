import pandas as pd
from sklearn.model_selection import train_test_split
from sklearn.ensemble import RandomForestRegressor
from sklearn.preprocessing import LabelEncoder
import pickle

df = pd.read_csv("weatherHistory.csv")
df.head()

df = df.dropna()

le = LabelEncoder()

# Encode the 'Summary' column as it contains categorical weather descriptions
df['Summary_encoded'] = le.fit_transform(df['Summary'])

# Ensure correct column names from the DataFrame are used
X = df[['Humidity', 'Wind Speed (km/h)']]   # inputs
y = df['Temperature (C)']                                     # output

X_train, X_test, y_train, y_test = train_test_split(X, y, test_size=0.2)

model = RandomForestRegressor()
model.fit(X_train, y_train)

with open("weather_model.pkl", "wb") as f:
    pickle.dump(model, f)